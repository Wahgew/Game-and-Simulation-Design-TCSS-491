        draw(ctx) {
                if (!ctx || !ctx.drawImage) {
                        console.error("Invalid context passed to testMap.draw:", ctx);
                        return;
                }

                this.#setCanvasSize(ctx.canvas);
                this.#clearCanvas(ctx);
                this.#drawMap(ctx);
        }

        #drawMap(ctx) {
                if (!ctx) return;

                for (let i = 0; i < this.map.length; i++) {
                        for (let j = 0; j < this.map[i].length; j++) {
                                const tile = this.map[i][j];
                                if (tile === 1 && this.block) {
                                        try {
                                                ctx.drawImage(this.block,
                                                    j * this.testSize,
                                                    i * this.testSize,
                                                    this.testSize,
                                                    this.testSize);
                                        } catch (e) {
                                                console.error("Error drawing tile:", e);
                                        }
                                }
                        }
                }
        }

        #clearCanvas(ctx) {
                if (!ctx) return;

                try {
                        ctx.fillStyle = "white";
                        ctx.fillRect(0, 0, ctx.canvas.width, ctx.canvas.height);
                } catch (e) {
                        console.error("Error in clearCanvas:", e);
                }
        }

        #setCanvasSize(canvas) {
                if (!canvas) return;
                canvas.height = this.map.length * this.testSize;
                canvas.width = this.map[0].length * this.testSize;
        }
